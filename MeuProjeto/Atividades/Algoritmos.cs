using System;
using System.Text;


namespace Algoritmos{

    public static class Calculos{

        public static int Soma(int a, int b){
            int resultado = a + b;
            return resultado;
        }

        public static int Sub(int a, int b){
            int resultado = a - b;
            return resultado;
        }

        public static int Mult(int a, int b){
            int resultado = a * b;
            return resultado;
        }

        public static int Div(int a, int b){
            int resultado = a / b;
            return resultado;
        }

    }
}
